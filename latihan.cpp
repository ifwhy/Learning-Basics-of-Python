#include <bits/stdc++.h>
using namespace std;

int main()
{
    struct titik {
        int x, y, z;
    }; 
    titik a, b, c;
    cin >> a.x >> b.x >> c.x;
    cin >> a.y >> b.y >> c.y;
    cin >> a.z >> b.z >> c.z;


    cout << a.x << " " << a.y << " " << a.z << " " << endl;
    cout << b.x << " " << b.y << " " << b.z << " " << endl;
    cout << c.x << " " << c.y << " " << c.z << " " << endl;
    return 0;
}
